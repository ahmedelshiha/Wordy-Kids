// Helper functions to filter words by category and difficulty
export const getWordsByCategory = (category: string): Word[] => {
  if (category === "all") return wordsDatabase;
  return wordsDatabase.filter((word) => word.category === category);
};

export const getWordsByDifficulty = (
  difficulty: "easy" | "medium" | "hard",
): Word[] => {
  return wordsDatabase.filter((word) => word.difficulty === difficulty);
};

export const getRandomWords = (
  count: number,
  category?: string,
  difficulty?: "easy" | "medium" | "hard",
): Word[] => {
  let filteredWords = wordsDatabase;

  if (category && category !== "all") {
    filteredWords = getWordsByCategory(category);
  }

  if (difficulty) {
    filteredWords = filteredWords.filter(
      (word) => word.difficulty === difficulty,
    );
  }

  // Shuffle and return requested count
  const shuffled = [...filteredWords].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getAllCategories = (): string[] => {
  const categories = [...new Set(wordsDatabase.map((word) => word.category))];
  return categories.sort();
};

export const searchWords = (query: string): Word[] => {
  const lowercaseQuery = query.toLowerCase();
  return wordsDatabase.filter(word => 
    word.word.toLowerCase().includes(lowercaseQuery) ||
    word.definition.toLowerCase().includes(lowercaseQuery) ||
    word.category.toLowerCase().includes(lowercaseQuery)
  );
};

export const getWordById = (id: number): Word | undefined => {
  return wordsDatabase.find(word => word.id === id);
};

export const getCategoryStats = () => {
  const stats: Record<string, { total: number; easy: number; medium: number; hard: number }> = {};
  
  categories.forEach(category => {
    if (category.id !== 'all') {
      const categoryWords = getWordsByCategory(category.id);
      stats[category.id] = {
        total: categoryWords.length,
        easy: categoryWords.filter(w => w.difficulty === 'easy').length,
        medium: categoryWords.filter(w => w.difficulty === 'medium').length,
        hard: categoryWords.filter(w => w.difficulty === 'hard').length
      };
    }
  });
  
  return stats;
};
