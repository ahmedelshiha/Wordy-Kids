// Helper functions for filtering and random selection
export const getWordsByCategory = (category: string): Word[] => {
  if (category === 'all') return wordsDatabase;
  return wordsDatabase.filter(word => word.category === category);
};

export const getWordsByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): Word[] => {
  return wordsDatabase.filter(word => word.difficulty === difficulty);
};

export const getRandomWords = (count: number, difficulty?: string): Word[] => {
  let filteredWords = wordsDatabase;
  if (difficulty) {
    filteredWords = wordsDatabase.filter(word => word.difficulty === difficulty);
  }
  
  const shuffled = [...filteredWords].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const searchWords = (searchTerm: string): Word[] => {
  const term = searchTerm.toLowerCase();
  return wordsDatabase.filter(word => 
    word.word.toLowerCase().includes(term) ||
    word.definition.toLowerCase().includes(term) ||
    word.category.toLowerCase().includes(term)
  );
};

export const getAllCategories = (): string[] => {
  const categories = [...new Set(wordsDatabase.map(word => word.category))];
  return categories.sort();
};

export const getWordCount = (): { total: number; byCategory: Record<string, number>; byDifficulty: Record<string, number> } => {
  const byCategory: Record<string, number> = {};
  const byDifficulty: Record<string, number> = {};
  
  wordsDatabase.forEach(word => {
    byCategory[word.category] = (byCategory[word.category] || 0) + 1;
    byDifficulty[word.difficulty] = (byDifficulty[word.difficulty] || 0) + 1;
  });
  
  return {
    total: wordsDatabase.length,
    byCategory,
    byDifficulty
  };
};
