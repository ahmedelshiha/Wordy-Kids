// Helper function to get words by category
export const getWordsByCategory = (category) => {
  if (category === "all") {
    return wordsDatabase;
  }
  return wordsDatabase.filter(word => word.category === category);
};

// Get all unique categories
export const getCategories = () => {
  const categories = [...new Set(wordsDatabase.map(word => word.category))];
  return categories.map(category => ({
    id: category,
    name: category.charAt(0).toUpperCase() + category.slice(1),
    emoji: getCategoryEmoji(category),
    wordCount: wordsDatabase.filter(word => word.category === category).length
  }));
};

// Helper function to get category emoji
